        private void addInvoice() {
        int id = Integer.parseInt(txtId.getText());
        Date date = new Date(txtDate.getText()); 
        Product selectedProduct = (Product) cmbProduct.getSelectedItem();
         
        float totalValue = selectedProduct.getCost() * selectedProduct.getAmount();
         ArrayList<Product> productsList = new ArrayList<>();
          productsList.add(selectedProduct);

          Invoice newInvoice = new Invoice(id, date, totalValue, productsList); 
          txtTotalValue.setText(String.valueOf(totalValue));
          
       
        
        
        MongoCollection<Document> collection = MDBManage.getFromCollection("Invoices", Document.class);
        Document invoiceDocument = new Document("id", newInvoice.getId())
                                    .append("date", newInvoice.getDateI())
                                    .append("boxes", newInvoice.getBoxes())
                                    .append("totalValue", newInvoice.getValue());
        collection.insertOne(invoiceDocument);

       
    }
      public ArrayList<Product> findAllProducts() {
        Class classType = Product.class;
        String collection = "Products";
        MongoCollection<Product> productDB = MDBManage.getFromCollection(collection, classType);
        ArrayList<Product> productList = new ArrayList<>();
        productDB.find().into(productList);
        return productList;
        
    }

    
    
